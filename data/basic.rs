/* 基本语法规则测试文件 */

// 1.1 基础程序（声明函数）
fn main(mut a:i32, mut b:i32) -> i32 {  // 1.4 形参，1.5 返回类型
    // 2.1 变量声明语句（包含 0.1 变量声明内部，0.2 类型）
    let mut x:i32;       
    let mut y;

    // 2.2 赋值语句（包含 0.3 可赋值元素）
    x = 42;
    y = x;

    // 2.3 变量声明赋值语句
    let mut z:i32 = 100;
    let mut w = 200;

    // 3.1 表达式语句：常数、变量、括号
    x;
    (y);
    ((42));

    // 3.2 运算表达式与比较表达式
    1 + 2 * 3;
    4 < 5;

    // 3.3 函数调用
    helper(1, 2);

    // 4.1 if-else 控制结构
    if x > 0 {
        y = y + 1;
    } else {
        y = y - 1;
    }

    // 5.1 while 循环
    while x > 0 {
        x = x - 1;
    }

    // 1.3 返回语句（空与含表达式）
    return y;
}

// 1.1 函数声明 + 1.2 语句串为空（只有 return;）+ 1.5 返回值为整数
fn helper(mut m:i32, mut n:i32) -> i32 {
    return m + n;
}